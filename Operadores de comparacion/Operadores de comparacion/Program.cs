//OPERADORES DE COMPARACION

int a = 10, b = 10;
string resultado;

//OPERADOR MAYOR, MENOR QUE
resultado = (a < b) ? "Si" : "No";
Console.WriteLine(resultado);

resultado = (a > b) ? "Si" : "No";
Console.WriteLine(resultado);

//OPERADOR IGUAL QUE
resultado = (a == b) ? "Si" : "No";
Console.WriteLine(resultado);

//MAYOR O IGUAL QUE
resultado = (a >= b) ? "Si" : "No";
Console.WriteLine(resultado);

//DIFERENTE QUE
resultado = (a != b) ? "Si" : "No";
Console.WriteLine(resultado);

